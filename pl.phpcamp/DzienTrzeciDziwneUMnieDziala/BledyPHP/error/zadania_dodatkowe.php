Zadanie 3.

Arkusz kalkulacyjny składa się z rzędów liczb pozornie losowych. Aby upewnić się, że proces odzyskiwania jest prawidłowy, musisz obliczyć sumę kontrolną arkusza kalkulacyjnego. Dla każdego rzędu określ różnicę między największą wartością a najmniejszą wartością; suma kontrolna jest sumą wszystkich tych różnic.

Na przykład, biorąc pod uwagę następujący arkusz kalkulacyjny:

5 1 9 5
7 5 3
2 4 6 8

Największe i najmniejsze wartości pierwszego rzędu to 9 i 1, a różnica ich wynosi 8.
Największe i najmniejsze wartości w drugim rzędzie to 7 i 3, a różnica między nimi to 4.
Różnica trzeciego rzędu to 6.

W tym przykładzie suma kontrolna arkusza kalkulacyjnego wyniesie 8 + 4 + 6 = 18.

