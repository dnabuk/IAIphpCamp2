Jednak elf z powrotem na biegunie północnym ma trochę za dużo ajerkoniaki, więc jego wskazówki są trochę poza zasięgiem, a Mikołaj kończy odwiedzanie kilku domów więcej niż raz. Ile domów otrzymuje co najmniej jeden prezent?
